package BaekJoon.Bronze.Stage1;

import java.util.Scanner;

public class Number6 {

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        double A = sc.nextInt();
        double B = sc.nextInt();
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.print(A % B);
    }
}
